    private void BtnTaBortActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnTaBortActionPerformed
        try {
            // Bekräftelse om användaren verkligen vill ta bort kundens uppgifter
            int svar = JOptionPane.showConfirmDialog(this, "Vill du verkligen ta bort kundens uppgifter?", "Bekräfta", JOptionPane.YES_NO_OPTION);

            if (svar == JOptionPane.YES_OPTION) {
                // SQL-fråga med rätt kolumnnamn
                String rensaKundUppgifter = "UPDATE Kund SET "
                        + "Fornamn = NULL, "
                        + "Efternamn = NULL, "
                        + "Matt = NULL, "
                        + "Epost = NULL, "
                        + "Telefonnummer = NULL, "
                        + "LeveransOrt = NULL, "
                        + "FakturaOrt = NULL, "
                        + "LeveransAdress = NULL, "
                        + "FakturaAdress = NULL, "
                        + "LeveransPostnummer = NULL, "
                        + "LeveransLand = NULL, "
                        + "FakturaPostnummer = NULL, "
                        + "FakturaLand = NULL "
                        + "WHERE KundID = " + kundID + ";";

                // Utför uppdateringen
                idb.update(rensaKundUppgifter);

                // Feedback till användaren
                JOptionPane.showMessageDialog(this, "Kundens uppgifter har rensats.");

                // Nollställ fälten
                txtFornamn.setText("");
                txtEfternamn.setText("");
                txtHuvudmatt.setText("");
                txtEpost.setText("");
                txtTelefonnummer.setText("");
                txtLeveransOrt.setText("");
                txtFakturaOrt.setText("");
                txtLeveransAdress.setText("");
                txtFakturaAdress.setText("");
                txtLeveransPostnummer.setText("");
                txtFakturaPostnummer.setText("");
                txtLeveransLand.setText("");
                txtFakturaLand.setText("");

                // Öppna AllaKunder och stäng nuvarande fönster
                new SeAllaKunder(idb, inloggadAnvandare).setVisible(true);

            }
        } catch (InfException ex) {
            JOptionPane.showMessageDialog(this, "Ett fel inträffade vid rensning av kundens uppgifter: " + ex.getMessage());
        }
    }//GEN-LAST:event_BtnTaBortActionPerformed

